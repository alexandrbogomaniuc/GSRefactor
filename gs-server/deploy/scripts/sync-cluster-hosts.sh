get_cfg() {
  local key="$1"
  awk -F= -v key="${key}" '
    /^[[:space:]]*#/ {next}
    /^[[:space:]]*$/ {next}
    {
      k=$1
      v=substr($0, index($0, "=") + 1)
      gsub(/^[[:space:]]+|[[:space:]]+$/, "", k)
      gsub(/^[[:space:]]+|[[:space:]]+$/, "", v)
      if (k == key) {
        print v
        exit
      }
    }
  ' "${SOURCE_FILE}"
}

